// WHILE
// checks condition then executes

let start = 0; // starting point
let end = 10;
while (start <= end) {
//   console.log(start);
  start = start + 2;
  // start = 0
  // start + 1 = 0 + 1 = 1
  // start = 1
}

console.log("------------------------------------");

// DO WHILE
// executes then checks condition

let s = 4;
let e = 25;

do {
//   console.log(s);
  s = s + 5;
} while (s <= e);


console.log("-----------------------------------")

